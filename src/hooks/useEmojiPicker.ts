const STORAGE_KEY = "blade-recent-emojis";
const MAX_RECENT = 30;

function loadRecent(): string[] {
  try { return JSON.parse(localStorage.getItem(STORAGE_KEY) || "[]"); }
  catch { return []; }
}

function saveRecent(recent: string[]) {
  localStorage.setItem(STORAGE_KEY, JSON.stringify(recent.slice(0, MAX_RECENT)));
}

export function useEmojiPicker() {
  const [recent, setRecent] = useState<string[]>(loadRecent);
  const [search, setSearch] = useState("");
  const [categoryId, setCategoryId] = useState("smileys");

  const allEmojis = useMemo(() => {
    return EMOJI_DATA.flatMap((cat) => cat.emojis.map((emoji) => ({ emoji, category: cat.id })));
  }, []);

  const filtered = useMemo(() => {
    if (!search.trim()) return null;
    const lower = search.toLowerCase();
    // Simple search: match emoji categories or filter all
    return allEmojis
      .filter(({ emoji, category }) => {
        return emoji.includes(search) || category.includes(lower);
      })
      .map((e) => e.emoji);
  }, [search, allEmojis]);

  const selectEmoji = useCallback((emoji: string) => {
    setRecent((prev) => {
      const next = [emoji, ...prev.filter((e) => e !== emoji)].slice(0, MAX_RECENT);
      saveRecent(next);
      return next;
    });
  }, []);

  const currentCategory = useMemo(() =>
    EMOJI_DATA.find((c) => c.id === categoryId) || EMOJI_DATA[0],
  [categoryId]);

  return {
    categories: EMOJI_DATA,
    currentCategory,
    setCategoryId,
    recent,
    search,
    setSearch,
    filtered,
    selectEmoji,
  };
}
